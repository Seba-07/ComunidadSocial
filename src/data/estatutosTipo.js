/**
 * Función para rellenar los estatutos con los datos de la organización
 * @param {string} tipoOrganizacion - Tipo de organización (ej: 'CENTRO_PADRES')
 * @param {Object} datos - Datos de la organización para rellenar
 * @returns {string} - Estatutos con los datos rellenados
 */
export function generarEstatutos(tipoOrganizacion, datos) {
  const estatuto = ESTATUTOS_TIPO[tipoOrganizacion];

  if (!estatuto) {
    throw new Error(`No existe estatuto tipo para: ${tipoOrganizacion}`);
  }

  let documento = estatuto.documento;

  // Reemplazar todos los placeholders con los datos proporcionados
  const placeholders = {
    '{{FECHA_DIA}}': datos.fechaDia || '____',
    '{{FECHA_MES}}': datos.fechaMes || '____',
    '{{FECHA_ANIO}}': datos.fechaAnio || '____',
    '{{HORA}}': datos.hora || '____',
    '{{DIRECCION}}': datos.direccion || '________________________________',
    '{{NUMERO}}': datos.numero || '________',
    '{{PRESIDENTE_REUNION}}': datos.presidenteReunion || '_______________________________',
    '{{SECRETARIO_REUNION}}': datos.secretarioReunion || '___________________________________',
    '{{NOMBRE_ESTABLECIMIENTO}}': datos.nombreEstablecimiento || '___________________________________________________',
    '{{NOMBRE_ORGANIZACION}}': datos.nombreOrganizacion || '___________________________________________________',
    '{{DEPOSITANTE}}': datos.depositante || '________________________________',
    '{{DOMICILIO}}': datos.domicilio || '________________________________________________',
    '{{MES_ASAMBLEA_1}}': datos.mesAsamblea1 || '________________',
    '{{MES_ASAMBLEA_2}}': datos.mesAsamblea2 || '_____________',
    '{{MES_INFORME}}': datos.mesInforme || '_________',
    '{{MES_ELECCION}}': datos.mesEleccion || '_________________',
    '{{CUOTA_INC_MIN}}': datos.cuotaIncMin || '_______',
    '{{CUOTA_INC_MAX}}': datos.cuotaIncMax || '_______',
    '{{CUOTA_ORD_MIN}}': datos.cuotaOrdMin || '____________',
    '{{CUOTA_ORD_MAX}}': datos.cuotaOrdMax || '_______',
    '{{ENTIDAD_DISOLUCION}}': datos.entidadDisolucion || '___________________________________________________________'
  };

  for (const [placeholder, valor] of Object.entries(placeholders)) {
    documento = documento.split(placeholder).join(valor);
  }

  return documento;
}

